function generateExportReport(audit: StoredAudit | null, recs: StoredRecommendation[]): string {
  if (!audit) return 'No audit data available.'
  const date = new Date(audit.created_at).toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })
  const lines: string[] = [
    `# SEO & GEO Audit Report`,
    `**Date:** ${date}`,
    `**Overall Score:** ${audit.overall_score}/100`,
    ``,
    `## Scores`,
    `| Category | Score |`,
    `|----------|-------|`,
    `| Technical SEO | ${audit.technical_score}/100 |`,
    `| Content Quality | ${audit.content_score}/100 |`,
    `| Structured Data | ${audit.structured_data_score}/100 |`,
    `| Performance | ${audit.performance_score}/100 |`,
    `| GEO Readiness | ${audit.geo_score}/100 |`,
    ``,
    `## Summary`,
    `- **Pages Audited:** ${audit.pages_audited}`,
    `- **Total Issues:** ${audit.total_issues}`,
    `- **Critical:** ${audit.critical_issues} | **High:** ${audit.high_issues} | **Medium:** ${audit.medium_issues} | **Low:** ${audit.low_issues}`,
    ``,
    `## Recommendations`,
    ``,
  ]

  const grouped: Record<string, StoredRecommendation[]> = {}
  recs.forEach(r => {
    const key = r.severity
    if (!grouped[key]) grouped[key] = []
    grouped[key].push(r)
  })

  const order = ['critical', 'high', 'medium', 'low'] as const
  order.forEach(severity => {
    const items = grouped[severity]
    if (!items?.length) return
    lines.push(`### ${severity.charAt(0).toUpperCase() + severity.slice(1)} (${items.length})`)
    lines.push(``)
    items.forEach(rec => {
      lines.push(`- **${rec.title}**${rec.page_path ? ` \`${rec.page_path}\`` : ''}`)
      lines.push(`  ${rec.description}`)
      if (rec.current_value) lines.push(`  - Current: ${rec.current_value}`)
      if (rec.recommended_value) lines.push(`  - Recommended: ${rec.recommended_value}`)
      lines.push(``)
    })
  })

  lines.push(`---`)
  lines.push(`*Generated by Julyu SEO & GEO Analytics*`)
  return lines.join('\n')
}

